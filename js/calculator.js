/**
 * Calculator module
 * @module calculator
 */

/**
 * Add two numbers
 * @param {number} num1 - First number
 * @param {number} num2 - Second number
 * @returns {number} - Sum of both numbers
 */
exports.add = (num1, num2) => num1 + num2;

/**
 * Multiply two numbers
 * @param {number} num1 - First number
 * @param {number} num2 - Second number
 * @returns {number} - Product of both numbers
 */
exports.multiply = (num1, num2) => num1 * num2;

/**
 * Subtract two numbers
 * @param {number} num1 - First number
 * @param {number} num2 - Second number
 * @returns  {number} - Difference of both numbers
 */
exports.subtract = (num1, num2) => num1 - num2;

/**
 * Divide two numbers
 * @param {number} num1 - First number
 * @param {number} num2 - Second number
 * @returns {number} - Quotient of both numbers
 */
exports.divide = (num1, num2) => num1 / num2;
